def luas(sisi: int | float) -> float:
    '''
    luas persegi dari rumus sisi x sisi

    sisi: nilai sisi dari persegi
    '''
    return sisi * sisi




def keliling(sisi: int | float) -> float:
    '''
    keliling persegi, nilai dari setiap sisi atau garis

    sisi: nilai dari sisi persegi
    '''
    return sisi * 4


